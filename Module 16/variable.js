// Creating Variable and Storing Values
// Keyword Name = Value;
var Roll = 7;
console.log(Roll);

// Once created the variable the value can be replaced but do not need to declare the variable again
Roll = 8;
console.log(Roll);

// Without providing Value a variable can be declared. If you print it will be undefined
var StudentID;
console.log(StudentID);

// TYPE
// Number
var roll = 2054901049; //Number type don't require double quotation
console.log(roll);
console.log(typeof roll); //This will show the type of the variable roll

// String
var name = "Mahbub Sarwar"; //String type requires double quotation or single quotation
var tag = 'Student';
console.log(name);
console.log(tag);

// Boolean
var MahbubIsRich = false; //Boolean type don't require double quotation and will be small caps
var MahbubIsGorib = true;
console.log(MahbubIsRich);
console.log(MahbubIsGorib);